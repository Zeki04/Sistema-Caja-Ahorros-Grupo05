@app.post(
    "/api/transacciones/deposito",
    response_model=DepositoResponse,
    status_code=status.HTTP_201_CREATED,
    tags=["Transacciones"]
)
async def registrar_deposito(payload: DepositoRequest, db: AsyncSession = Depends(get_db)):
    """
    Permite registrar un depósito financiero en la cuenta de ahorro de un socio
    y actualiza su saldo correspondiente bajo una transacción segura.
    """
    # 1. Buscar si el socio existe
    resultado_socio = await db.execute(select(Socio).where(Socio.id == payload.idSocio))
    socio = resultado_socio.scalar_one_or_none()
    if not socio:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Socio con ID {payload.idSocio} no encontrado"
        )

    # 2. Buscar la cuenta activa del socio
    resultado_cuenta = await db.execute(
        select(Cuenta).where(Cuenta.socio_id == payload.idSocio, Cuenta.estado == "Activa")
    )
    cuenta = resultado_cuenta.scalar_one_or_none()
    if not cuenta:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="El socio no posee ninguna cuenta de ahorro activa registrada"
        )

    # 3. Registrar el Asiento Contable (Partida Doble)
    monto_decimal = Decimal(str(payload.monto))
    nuevo_asiento = Asiento(
        cuenta_id=cuenta.id,
        tipo_movimiento="CREDITO",  # Un depósito incrementa la cuenta
        monto=monto_decimal,
        descripcion=payload.descripcion
    )
    db.add(nuevo_asiento)

    # 4. Actualizar el saldo de la cuenta
    cuenta.saldo += monto_decimal
    db.add(cuenta)

    # Forzar el guardado y refrescar para obtener el ID generado del asiento
    await db.flush()

    return DepositoResponse(
        mensaje="Depósito registrado correctamente",
        idTransaccion=nuevo_asiento.id,
        nuevoSaldo=float(cuenta.saldo)
    )


@app.get(
    "/api/socios/{id}/saldo",
    response_model=SaldoResponse,
    tags=["Socios"]
)
async def consultar_saldo(id: int, db: AsyncSession = Depends(get_db)):
    """
    Obtiene el saldo actual disponible de la cuenta de ahorro de un socio específico.
    """
    # 1. Buscar socio y su cuenta asociada
    resultado = await db.execute(
        select(Socio).where(Socio.id == id)
    )
    socio = resultado.scalar_one_or_none()
    if not socio:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Socio con ID {id} no existe en el sistema"
        )

    resultado_cuenta = await db.execute(
        select(Cuenta).where(Cuenta.socio_id == id)
    )
    cuenta = resultado_cuenta.scalar_one_or_none()
    saldo_disponible = float(cuenta.saldo) if cuenta else 0.0

    return SaldoResponse(
        idSocio=socio.id,
        nombre=f"{socio.nombre} {socio.apellido}",
        saldoDisponible=saldo_disponible
    )


