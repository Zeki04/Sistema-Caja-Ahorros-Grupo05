import os
import datetime
from decimal import Decimal
from typing import List
from dotenv import load_dotenv
from fastapi import FastAPI, Depends, HTTPException, status
from pydantic import BaseModel, Field
from sqlalchemy import String, Numeric, ForeignKey, DateTime, select
from sqlalchemy.ext.asyncio import AsyncSession, create_async_engine, async_sessionmaker
from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column, relationship

# Cargar variables de entorno
load_dotenv()
DATABASE_URL = os.getenv("DATABASE_URL")

engine = create_async_engine(DATABASE_URL, echo=True)
AsyncSessionLocal = async_sessionmaker(bind=engine, class_=AsyncSession, expire_on_commit=False)

class Base(DeclarativeBase):
    pass

# --- MODELOS ---
class Socio(Base):
    __tablename__ = "socios"
    id: Mapped[int] = mapped_column(primary_key=True, index=True)
    cedula: Mapped[str] = mapped_column(String(10), unique=True, index=True, nullable=False)
    nombre: Mapped[str] = mapped_column(String(50), nullable=False)
    apellido: Mapped[str] = mapped_column(String(50), nullable=False)
    email: Mapped[str] = mapped_column(String(100), unique=True, nullable=False)
    fecha_registro: Mapped[datetime.datetime] = mapped_column(DateTime, default=datetime.datetime.utcnow)
    cuentas: Mapped[List["Cuenta"]] = relationship("Cuenta", back_populates="socio", cascade="all, delete-orphan")

class Cuenta(Base):
    __tablename__ = "cuentas"
    id: Mapped[int] = mapped_column(primary_key=True, index=True)
    numero_cuenta: Mapped[str] = mapped_column(String(20), unique=True, index=True, nullable=False)
    tipo_cuenta: Mapped[str] = mapped_column(String(30), default="Ahorro")
    saldo: Mapped[Decimal] = mapped_column(Numeric(12, 2), default=Decimal("0.00"))
    estado: Mapped[str] = mapped_column(String(20), default="Activa")   
    socio_id: Mapped[int] = mapped_column(ForeignKey("socios.id"), nullable=False)
    socio: Mapped["Socio"] = relationship("Socio", back_populates="cuentas")
    asientos: Mapped[List["Asiento"]] = relationship("Asiento", back_populates="cuenta", cascade="all, delete-orphan")

class Asiento(Base):
    __tablename__ = "asientos"
    id: Mapped[int] = mapped_column(primary_key=True, index=True)
    cuenta_id: Mapped[int] = mapped_column(ForeignKey("cuentas.id"), nullable=False)
    tipo_movimiento: Mapped[str] = mapped_column(String(10), nullable=False)
    monto: Mapped[Decimal] = mapped_column(Numeric(12, 2), nullable=False)
    descripcion: Mapped[str] = mapped_column(String(255), nullable=True)
    timestamp: Mapped[datetime.datetime] = mapped_column(DateTime, default=datetime.datetime.utcnow)
    cuenta: Mapped["Cuenta"] = relationship("Cuenta", back_populates="asientos")

